# perulangan (loop)

'''
for kondisi :
    aksi
'''
# ini dengan list
angka2_list = [0,2,4,6,8] # ini adalah list
print(angka2_list)

for i in angka2_list:
    print(f"i sekarang -> {i}")

print("Akhir Dari Program 1 \n")


# ini dengan range
angka2_range = range(5)
for i in angka2_range:
    print(f"i sekarang -> {i}")

print("Akhir Dari Program 2 \n")
